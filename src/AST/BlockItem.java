package AST;

/**
 * 代码块项目抽象基类 - 编译器中的"组织单元"
 * 
 * 这个类代表可以出现在代码块中的所有元素，就像建筑中的"标准模块"。
 * 在C语言的代码块（用大括号{}包围的区域）中，可以包含：
 * 
 * 1. 声明语句 (Decl) - 就像在房间里"放置家具"
 * - 变量声明：int x;
 * - 常量声明：const int MAX = 100;
 * 
 * 2. 执行语句 (Stmt) - 就像在房间里"进行活动"
 * - 赋值语句：x = 5;
 * - 条件语句：if (x > 0) {...}
 * - 循环语句：while (x < 10) {...}
 * - 函数调用：printf("Hello");
 * 
 * 这个基类确保了代码块中的所有元素都遵循统一的处理规范，
 * 就像确保所有模块都符合建筑标准一样。
 */
public class BlockItem extends Node {

    /**
     * 代码块项目的默认构造函数
     * 
     * 作为抽象基类，为声明和语句提供共同的基础。
     * 所有能够放入代码块的元素都继承这个类。
     */
    public BlockItem() {
        // 基类默认构造函数
        // 子类会实现具体的项目类型逻辑
    }
}
